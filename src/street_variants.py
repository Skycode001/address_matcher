# Создаем словарь для быстрой замены
REVERSE_MAP = {}
for street in STREET_NAMES:
    words = street.split()
    if len(words) >= 2:
        reversed_street = ' '.join(words[::-1])
        REVERSE_MAP[reversed_street] = street


def reverse_street_words(text):
    if not text:
        return text

    # Заменяем ё на е для унификации
    result = text.lower().replace('ё', 'е')

    for reversed_street, correct_street in REVERSE_MAP.items():
        if reversed_street in result:
            result = result.replace(reversed_street, correct_street)

    return result

